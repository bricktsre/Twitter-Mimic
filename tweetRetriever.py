	def calculateMeanAndStdDev(self, dataset, count):
		mean = sum(dataset) / count
		total = 0
		for item in dataset:
			total += (item - mean) ** 2
		std = math.sqrt(total / count)
		return mean, std

	def cleanAndCount(self):
		tweetLengths = []
		count = 0
		file = open(self.outfile, "a+")
		for tweet in self.tweets:
			tweet = self.removeLinks(tweet.text) + ' '
			if tweet == " ":
				continue
			tweetLengths.append(len(tweet))
			count += 1
			file.write(tweet)
		file.close()
		return tweetLengths, count
