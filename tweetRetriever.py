class TweetRetriever:
	tweetLength = None

	def __init__(self, outfile):
		self.outfile = outfile
		if os.path.exists(self.outfile):
			os.remove(self.outfile)

	def getTweets(self,handle):
		dataset = []
		total = 0
		for year in range(2006,2021):
			print("Starting to pull tweets from ", year)
			sinceDate = str(year) + "-01-01"
			untilDate = str(year+1) + "-01-01"
			since = datetime.strptime(sinceDate, '%Y-%m-%d')
			until = datetime.strptime(untilDate, '%Y-%m-%d')
			tweetCriteria = got.manager.TweetCriteria().setUsername(handle)\
							   .setEmoji("unicode")\
							   .setSince(sinceDate)\
							   .setUntil(untilDate)
			self.tweets = got.manager.TweetManager.getTweets(tweetCriteria)
			lengths, count = self.cleanAndCount()
			dataset.extend(lengths)
			total += count
			print("Pulled down ", count," tweets from ", year, ", sleeping for 15 minutes before trying next year")
			if count > 0:
				time.sleep(900)
		return self.calculateMeanAndStdDev(dataset, total)

